---WIDOK NAJCZĘŚCIEJ WYBIERANEGO APARTAMENTU WŚRÓD WSZYSTKICH ZAKOŃCZONYCH REZERWACJI--
CREATE VIEW TOP_AP_ZAK_REZ AS
SELECT APARTAMENT_ID, ILE_RAZY
FROM(
    SELECT APARTAMENT_ID,
        COUNT(*) AS ILE_RAZY,
        DENSE_RANK() OVER (ORDER BY COUNT(*) DESC) AS RANGA
        FROM REZERWACJE
        WHERE STATUS_ID = 4
        GROUP BY APARTAMENT_ID
    )
WHERE RANGA = 1
WITH READ ONLY;

SELECT * FROM TOP_AP_ZAK_REZ

---WIDOK TOP 3 NAJCZĘŚCIEJ WYBIERANYCH APARTAMENTÓW WŚRÓD WSZYSTKICH ZAKOŃCZONYCH REZERWACJI--
CREATE VIEW TOP3_AP_ZAK_REZ AS
SELECT APARTAMENT_ID, ILE_RAZY
FROM(
    SELECT APARTAMENT_ID,
        COUNT(*) AS ILE_RAZY,
        DENSE_RANK() OVER (ORDER BY COUNT(*) DESC) AS RANGA
        FROM REZERWACJE
        WHERE STATUS_ID = 4
        GROUP BY APARTAMENT_ID
    )
WHERE RANGA <= 3
ORDER BY RANGA, APARTAMENT_ID
WITH READ ONLY;

SELECT * FROM TOP3_AP_ZAK_REZ


---TOP3 najczęstsze państwa wśród zakończonych rezerwacji---
CREATE VIEW TOP3_PANSTWA_ZAK_REZ AS
WITH TOP_PAN AS (
    SELECT PANSTWO_ID,
        COUNT(*) AS LICZBA_PANSTW,
        DENSE_RANK() OVER (ORDER BY COUNT(*) DESC) AS PAN_RANGA
    FROM REZERWACJE r
    JOIN KLIENCI k ON r.KLIENT_ID = k.KLIENT_ID
    JOIN ADRESY a ON k.ADRES_ID = a.ADRES_ID
    WHERE r.STATUS_ID = 4
    GROUP BY a.PANSTWO_ID
)
SELECT PANSTWO_ID, LICZBA_PANSTW
FROM TOP_PAN ps
WHERE PAN_RANGA <=3
ORDER BY LICZBA_PANSTW DESC
WITH READ ONLY;

SELECT * FROM TOP3_PANSTWA_ZAK_REZ

